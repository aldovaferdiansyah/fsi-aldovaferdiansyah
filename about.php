                            Fakultas Sains dan Informatika (FSI) merupakan salah satu Fakultas tertua di Unjani yang terbentuk tahun 1988 dengan nama Sekolah Tinggi Matematika dan Ilmu Pengetahuan Alam (STMIPA), sejak tahun 1990 institusi yang berada dibawah naungan Yayasan Kartika Eka Paksi (YKEP) dilebur menjadi Universitas Jenderal Achmad Yani dengan SK Nomor: Skep/-027/YKEP/1990 dan SK Mendikbud Nomor: 0513/1990. Maka sejak tahun 1990 STMIPA berubah menjadi Fakultas Matematika dan Ilmu Pengetahuan Alam (FMIPA), seiring dengan perkembangan sains dan teknologi maka sejak 10 November 2017 nama Fakultas ini berubah menjadi Fakultas Sains dan Informatika (FSI) dengan SK Rektor Unjani Nomor: Skep/2116/UNJANI/XI/2017. 
